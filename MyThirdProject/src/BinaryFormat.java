
public class BinaryFormat {

	public static void main(String[] args) {
		// how to print binary out
		int x =0B101; //5 in binary is 101
		int y =0B1010100000;
		int z=0B100_00_00_00_00;//we can use under score to separate zero
		//we should put zero and B before the binary number
		System.out.println(x);
		System.out.println(y);
		System.out.print(z);
		

	}

}
